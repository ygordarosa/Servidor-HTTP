package server;

/**
 *
 * @author Caroline
 */
public class Arquivos {
    private String nome;
    private String tipo;
    
    public Arquivos(String nome, String tipo) {
        this.nome = nome;
        this.tipo = tipo;
    }

    public String getNome() {
        return nome;
    }

    public String getTipo() {
        return tipo;
    }

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }

}
